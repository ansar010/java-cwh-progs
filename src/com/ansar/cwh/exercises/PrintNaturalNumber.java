package com.ansar.cwh.exercises;

import java.util.Scanner;

/**
 * Implement a program to print natural number.
 */
public class PrintNaturalNumber {
    public static void main(String[] args) {
        int from,to;
        from = 100;
        to = 200;

        while (from  <= to) {
            System.out.println(from);
            from++;
        }

    }
}
